// For tools not in toolActions, generate a generic action from the translated tool name
function getToolAction(slug: string, locale: Locale): string {
  if (toolActions[slug]?.[locale]) return toolActions[slug][locale];
  const name = getToolName(slug, locale) || slug;
  const genericActions: Record<Locale, (n: string) => string> = {
    en: (n) => `Use the ${n}`,
    es: (n) => `Usa el ${n}`,
    fr: (n) => `Utilisez le ${n}`,
    de: (n) => `Nutzen Sie den ${n}`,
    pt: (n) => `Use o ${n}`,
    ja: (n) => `${n}を使用`,
    zh: (n) => `使用${n}`,
    ko: (n) => `${n} 사용`,
    it: (n) => `Usa il ${n}`,
    hi: (n) => `${n} का उपयोग करें`,
  };
  return genericActions[locale](name);
}

export function getTranslatedToolContent(slug: string, locale: Locale): ToolContent {
  const name = getToolName(slug, locale) || slug;
  const action = getToolAction(slug, locale);
  const patterns = howToUsePatterns[locale];
  const faq = faqPatterns[locale];

  return {
    intro: introPatterns[locale](name, action),
    howToUse: [patterns.enter, patterns.see, patterns.copy],
    faqs: [
      faq.howDoesItWork(name),
      faq.isItFree,
      faq.isDataSafe,
    ],
  };
}
